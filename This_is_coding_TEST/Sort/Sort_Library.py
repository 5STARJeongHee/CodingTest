# 병합 정렬로 구현되어 있고, 최악의 경우에도 O(NlogN)을 보장

# 코딩 테스트에서 정렬 알고리즘에 대한 문제 유형은 3가지

# 1. 정렬 라이브러리로 풀 수 있는 문제: 단순히 정렬 기법을 알고 있는지
# 물어보는 것으로 정렬 라이브러리를 알고 있다면 어렵지 않게 풀 수 있음

# 2. 정렬 알고리즘의 원리에 대해서 물어보는 문제: 선택 정렬, 삽입 정렬, 퀵 정렬 등의
# 원리를 알아야 문제를 풀 수 있음

# 3. 더 빠른 정렬이 필요한 문제: 퀵 정렬 기반의 정렬 기법으로는 풀 수 없으며 
# 계수 정렬 등의 다른 정렬 알고리즘을 이용하거나 문제에서 기존에 알려진 
# 알고리즘의 구조적인 개선을 거쳐야 풀 수 있다.